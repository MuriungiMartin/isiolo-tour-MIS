<h3>Organization</h3>
The reserve was established in 1948 as part of the Samburu - Isiolo Game Reserve, and the present boundaries were established in 1985.<br> The reserve is managed by the Isiolo County.<br> Most operators of Kenyan safaris offer a visit to the reserve, which has several Safari Lodges and safari camps. <br><br>

<h3>Location</h3>
The Buffalo Springs National Reserve is south of the Samburu National Reserve, which lies on the other side of the Ewaso Nyiro river. <br>It is named after an oasis of clear water at its western end. The reserve has an area of 131 square kilometres (51 sq mi), and is at an altitude of between 850 metres (2,790 ft) and 1,230 metres (4,040 ft) above sea level.<br>It is a gently rolling lowland plain of old lava flows and volcanic soils of olivine basalt. The main feature is the Champagne Ride in the southeast, an ancient lava-terrace.<br> The climate is hot, dry and semi-arid.<br><br>

<h3>Flora</h3>
There is a narrow band of riverine forest along the Ewaso Nyiro which includes Tana River Poplar, Doum Palm and magnificent specimens of Acacia elatior.<br> Vegetation includes acacia tortilis woodland and large stretches of bushland dominated by Commiphora. In some areas lava rock is exposed, with scattered grass and shrubs.<br> Other parts have alkaline grasslands with occasional springs and swamps. Here and there the "Desert Rose" (Adenium obesum) is found in the scrub, with bright pink blooms.<br> The Salvadora persica (tooth-brush tree) shrub provides food to elephants, and its twigs are used as toothbrushes by the nomadic Samburu people. <br>

<h3>Fauna</h3>
Wildlife include Grant's zebra and the endangered Grevy's Zebra. Other species of mammal include reticulated giraffe, the African bush elephant, oryx, gerenuk, African buffalo, lion, leopard, cheetah and hyena. Over 365 species of bird have been recorded in the reserve.<br> The river is home to hippopotamuses and crocodiles. Somali ostriches are widespread within the national reserve. It is larger than the Masai ostrich's and is distinctive for their indigo legs and neck.<br><br>
