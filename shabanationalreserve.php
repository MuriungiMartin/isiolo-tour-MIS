<h4>Location</h4>
The Shaba National Reserve was gazetted in 1974. It is administered by the Isiolo County government.<br>  It is just east of Samburu and 70 kilometres (43 mi) to the north of Mount Kenya.<br>  The Ewaso Nyiro river runs for 34 kilometres (21 mi) along the northern boundary of the reserve.<br>  Annual rainfall is between 250 millimetres (9.8 in) and 500 millimetres (20 in). The soils are sandy, volcanic in origin.<br> The reserve is semi desert, dotted with isolated hills and plentiful springs. Shaba Hill in the south, with its volcanic formations, rises to 2,145 metres (7,037 ft) above sea level from relatively flat surrounding country.<br>  The land at the foot of this hill is rugged, containing steep ravines.<br> <br> 

Shaba is smaller than Samburu Reserve and has more riverine forests, which contain acacia elatior and doum palms.<br>  Away from the river the habitats include umbrella thorn acacia woodland, bush land dominated by commiphora, alkaline grasslands and open areas of lava rock that contain scattered patches of grass and shrubs.<br> <br> 

<h4>Fauna</h4>
Although Shaba is greener than Samburu, game species such as Masai giraffes and Grant's zebras are less common.<br>  There are many klipspringer and hyrax in the hills. Aardvarks, warthogs and bat-eared foxes make their homes in domed termite mounds in the shrubland.<br>  Common eland, Impala, Grant's gazelle (Bright's sub-species) and gerenuk graze the shrubs, and zebras, Beisa oryx, greater kudu and lesser kudu graze in the grasslands.<br>  Shaba is well known for the large lion prides, which rest under thickets of toothbrush trees during the day. At night, predators include the black-backed jackal, striped hyena and spotted hyena<br>  The reserve is home to rare species that include the reticulated giraffe, Somali ostrich and the endangered Grevy's zebra.<br>  Other fauna include African leopards and elephants.<br> <br> 

Bird life is plentiful in Shaba, and similar to that of the Samburu and Buffalo Springs National Reserves.<br>  The near-threatened and poorly known Williams's lark is found in the reserve in regions of rocky lava semi-desert with low Barleria shrubs. It has not been observed in any other protected area.<br>  The reserve lies on the migration route from the Palearctic for the globally threatened lesser kestrel, a few of whom pass through each year. <br> Shaba is also home to regionally threatened birds that include the sporadically visiting African darter and great egret and the resident white-headed vulture, martial eagle and yellow-billed oxpecker, the last of which is fairly common.<br> <br> 
