	<p>Shaba National Reserve is a protected area in northern Kenya to the east of the Samburu and Buffalo Springs national reserves.<br> Together, the three reserves form a large protected area.<br><br>

The Shaba reserve has dramatic scenery including river-side forests, scattered woodlands and dry grasslands dominated by the Shaba Hill volcano.<br> The plentiful wildlife relies on waterholes and marshes scattered throughout the reserve.<br> Shaba is home to the endangered Grevy's zebra and the rare Williams's lark. Shaba was the setting for the book and film Born Free, for the film Out of Africa and for the reality show Survivor: Africa.<br><br>

The reserve is a popular destination for tourists.<br><br>

