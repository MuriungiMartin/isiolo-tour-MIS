	<p>
		Meru National Park is a Kenyan national park located east of Meru, 350 km (220 mi) from Nairobi. Covering an area of 870 km2 (340 sq mi), it is one best known national parks in Kenya.<br>Rainfall in this area is abundant with 635–762 mm (25.0–30.0 in) in the west of the park and 305–356 mm (12.0–14.0 in) in the east. The rainfall results in tall grass and lush swamps.<br><br>

The park has a wide range of wild animals including the African bush elephant, lion, African leopard, cheetah, eastern black rhinoceros, southern white rhinoceros, Grévy's zebra, hippopotamus.<br><br>

Meru was one of the two areas in which conservationists George Adamson and Joy Adamson raised Elsa the Lioness made famous in the best selling book and award winning movie Born Free. Elsa the Lioness is buried in this park and part of Joy's ashes were scattered on her gravesite.<br>
Some part of this Park covers Isiolo County. <br>
